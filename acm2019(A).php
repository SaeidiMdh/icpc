<?php
$n = (int) readline('');// کل بنزین مورد نیاز
$k =(int) readline('');//سهمیه که با ۱۵۰۰ حساب میشه
if ($n<=60 or $k+60>=$n ){
echo ("1500" * $n);
}else{
    $bs  = $n-60; // سهم ماهیانه برداشته میشه
    $bs -= $k; // سهم اضافه ماه قبل برداشته میشه
    echo ((abs($n-abs($bs))*1500)+($bs*3000));
    
}